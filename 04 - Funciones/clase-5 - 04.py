#4 Escribir una función que calcule el área de un rectángulo. La función recibe la base y la altura y retorna el área. 

def calcular_area_rectangulo(base, altura):
    area = base * altura
    return area

base = 12
altura = 10
area_calculada = calcular_area_rectangulo(base, altura)
print(f"El area del rectángulo es: {area_calculada}")

